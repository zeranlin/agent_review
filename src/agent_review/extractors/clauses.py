from __future__ import annotations

from ..models import ExtractedClause


def extract_clauses(text: str) -> list[ExtractedClause]:
    patterns = [
        ("项目基本信息", "项目名称", ["项目名称"]),
        ("项目基本信息", "项目编号", ["项目编号"]),
        ("项目基本信息", "采购方式", ["采购方式", "公开招标", "竞争性磋商", "竞争性谈判"]),
        ("项目基本信息", "项目属性", ["货物", "工程", "服务"]),
        ("项目基本信息", "预算金额", ["预算金额"]),
        ("项目基本信息", "最高限价", ["最高限价"]),
        ("项目基本信息", "合同履行期限", ["合同履行期限"]),
        ("资格条款", "一般资格要求", ["资格要求", "供应商资格"]),
        ("资格条款", "特定资格要求", ["特定资格要求", "资质要求"]),
        ("资格条款", "信用要求", ["信用要求"]),
        ("资格条款", "是否允许联合体", ["联合体"]),
        ("资格条款", "是否允许分包", ["分包"]),
        ("技术条款", "是否指定品牌", ["品牌", "原厂"]),
        ("技术条款", "是否要求专利", ["专利"]),
        ("技术条款", "是否要求检测报告", ["检测报告"]),
        ("技术条款", "是否要求认证证书", ["认证证书", "证书"]),
        ("技术条款", "是否设置★实质性条款", ["★"]),
        ("技术条款", "是否有限制产地厂家商标", ["产地", "厂家", "商标"]),
        ("评分条款", "评分方法", ["评分方法", "综合评分", "评标办法"]),
        ("评分条款", "证书加分", ["证书加分", "证书"]),
        ("评分条款", "业绩加分", ["业绩加分", "业绩"]),
        ("评分条款", "方案评分", ["方案评分", "实施方案"]),
        ("评分条款", "售后加分", ["售后"]),
        ("评分条款", "财务指标加分", ["财务指标", "利润率"]),
        ("合同条款", "付款节点", ["付款方式", "付款节点"]),
        ("合同条款", "验收标准", ["验收标准", "验收"]),
        ("合同条款", "争议解决方式", ["争议解决"]),
        ("合同条款", "违约责任", ["违约责任"]),
        ("合同条款", "质保期", ["质保期"]),
        ("合同条款", "履约保证金", ["履约保证金"]),
        ("政策条款", "是否专门面向中小企业", ["中小企业"]),
        ("政策条款", "所属行业划分", ["所属行业"]),
        ("政策条款", "是否仍保留价格扣除条款", ["价格扣除"]),
        ("政策条款", "是否涉及进口产品", ["进口产品"]),
    ]
    lines = text.splitlines()
    clauses: list[ExtractedClause] = []
    for category, field_name, keywords in patterns:
        for line_no, line in enumerate(lines, start=1):
            if any(keyword in line for keyword in keywords):
                clauses.append(
                    ExtractedClause(
                        category=category,
                        field_name=field_name,
                        content=line[:120],
                        source_anchor=f"line:{line_no}",
                    )
                )
                break
    return clauses
